package com.basic.bai1;

public class CalculateUtils {

	static double x;
	static double y;
	
	public static double tinhCong() {
		return x+y;
	}
	
	public static double tinhNhan() {
		return x*y;
	}

	public static double tinhTru() {
		return x-y;
	}

	public static double tinhChia() {
		return x/y;
	}	

}
